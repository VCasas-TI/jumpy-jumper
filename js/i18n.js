function jjApplyI18N(lang) {
  if (!lang) lang = jjGetLang();
  const dict = window.JJ_I18N[lang] || window.JJ_I18N.en;

  document.documentElement.setAttribute("data-lang", lang);
  document.documentElement.setAttribute("lang", lang);

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key] !== undefined) el.setAttribute("placeholder", dict[key]);
  });

  document.querySelectorAll("[data-cat]").forEach(el => {
    const cat = el.getAttribute("data-cat");
    const catDict = window.JJ_CATEGORIES[lang] || window.JJ_CATEGORIES.en;
    if (catDict[cat]) el.textContent = catDict[cat];
  });

  try { localStorage.setItem("jj_lang", lang); } catch (e) {}

  document.dispatchEvent(new CustomEvent("jj:lang-changed", { detail: { lang } }));
}

function jjGetLang() {
  try {
    const saved = localStorage.getItem("jj_lang");
    if (saved === "en" || saved === "es") return saved;
  } catch (e) {}
  const browserLang = (navigator.language || "en").toLowerCase();
  return browserLang.startsWith("es") ? "es" : "en";
}

function jjSetLang(lang) {
  jjApplyI18N(lang);
}
